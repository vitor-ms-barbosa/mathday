"use script"

let hh = 0;
let mm = 0;
let ss = 0;
let tempo_s = 1000;
let contador;

let add = 5;

function start() {
	contador = setInterval(() => {timer();}, tempo_s);
}

function stop() {
	clearInterval(contador);
	hh = 0;
	mm = 0;
	ss = 0;
}

function pause() {
	clearInterval(contador);
}

function timer(){

	ss++;

	if (ss > 60) {
		ss = 0;
		mm++;
		if (mm > 60) {
			mm = 0;
			hh++;
		}
		if (mm == add) {
			pause();
			document.getElementById('add_time').style.display = "flex";
			document.getElementById('retomar').style.display = "none";
			document.getElementById('pausar').style.display = "none";
			document.getElementById('contador').style.color = "red";
		}
	}
	formato = (hh < 10 ? '0' + hh : hh) + ":" + (mm < 10 ? '0' + mm : mm ) + ":" + (ss < 10 ? '0' + ss : ss);
	document.getElementById('contador').innerHTML = formato;
}


